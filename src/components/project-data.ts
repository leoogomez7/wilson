const uniqueGalleryBySrc = (gallery: CasaGalleryItem[]) =>
  gallery.reduce(
    (acc, item) => {
      if (!acc.some((existing) => existing.src === item.src)) {
        acc.push(item);
      }
      return acc;
    },
    [] as CasaGalleryItem[]
  );

